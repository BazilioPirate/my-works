console.log("Дробная часть второго числа:", secondNormalized);
console.log("Дробные части чисел равны?", firstNormalized === secondNormalized);
console.log("Дробная часть первого числа больше?", firstNormalized > secondNormalized);
console.log("Дробная часть первого числа меньше?", firstNormalized < secondNormalized);
console.log("Дробная часть первого числа больше или равна дробной части второго числа?", firstNormalized >= secondNormalized);
console.log("Дробная часть первого числа меньше или равна дробной части второго числа?", firstNormalized <= secondNormalized);
console.log("Дробная часть первого числа не равна дробной части второго?", firstNormalized !== secondNormalized);

